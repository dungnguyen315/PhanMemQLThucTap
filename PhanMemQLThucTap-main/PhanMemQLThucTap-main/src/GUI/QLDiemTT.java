    private String xepLoai(){
        String diem = txtDiem.getText().toString().trim();
        double diemTT = Double.parseDouble(diem);
        if(diemTT<5){
            return "Yếu";
        }else if(diemTT<=6.9){
            return "Trung bình";
        }else if(diemTT<=8.4){
            return "Khá";
        }else{
            return "Giỏi";
        }
    }
    boolean kiemtra(String kt){
       boolean x=true;
       for (var it:dsDiemTT){
           if(it.getMaSV().trim().equals(kt.trim()))
               x=false;
       } 
       return  x;
    }
     
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String diem = txtDiem.getText().toString().trim();
        if(diem.isEmpty()){
           JOptionPane.showMessageDialog(this,"Điểm không được để trống !");
        }else{
            double diemTT = Double.parseDouble(diem);
           if(diemTT<0){
           JOptionPane.showMessageDialog(this,"Điểm không hợp lệ !");
          }else{
               if(kiemtra(maSV)){
                   diemSV = new DiemTT(maSV,diem,xepLoai());
                   dsDiemTT.add(diemSV);
                   luuFileDiem();
                   loadTable();
               }else{
             JOptionPane.showMessageDialog(this,"Sinh viên đã được thêm điểm !");   
               }         
            }
        }     
    }//GEN-LAST:event_btnThemActionPerformed
    
    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        dongClick = jTableDiem.getSelectedRow();
        String diem = txtDiem.getText().toString().trim();
        if (dongClick != -1) {
            if (diem.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Điểm không được để trống !");
            } else {
                double diemTT = Double.parseDouble(diem);
                if (diemTT < 0) {
                    JOptionPane.showMessageDialog(this, "Điểm không hợp lệ !");
                } else {
                    if (kiemtra(maSV)==false) {
                        if(maSV.equals(dsDiemTT.get(dongClick).getMaSV().trim())){
                            diemSV = new DiemTT(maSV, diem, xepLoai());
                            dsDiemTT.set(dongClick,diemSV);
                            luuFileDiem();
                            loadTable();
                        }else {
                            JOptionPane.showMessageDialog(this, "Sinh viên đã được thêm điểm !");
                        }                       
                    }else{
                            diemSV = new DiemTT(maSV, diem, xepLoai());
                            dsDiemTT.set(dongClick,diemSV);
                            luuFileDiem();
                            loadTable();
                    }
                }
           }
      }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        dongClick = jTableDiem.getSelectedRow();      
        if(dongClick != -1){
            int check = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa không ?"
                    ,"Dialog Confirm",JOptionPane.YES_NO_OPTION);
            if(check==JOptionPane.YES_OPTION){
                dsDiemTT.remove(dongClick);
                luuFileDiem();
                loadTable();
            }          
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSapXepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSapXepActionPerformed
        Collections.sort(dsDiemTT,new Comparator<DiemTT>(){
            @Override
            public int compare(DiemTT o1, DiemTT o2) {   
                int d1 = Integer.parseInt(o1.getDiem());
                int d2 = Integer.parseInt(o2.getDiem());
              return d1 > d2 ? 1:-1;
            }
        });
       loadTable();
    }//GEN-LAST:event_btnSapXepActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String maSVSearch = txtTimKiem.getText().toString().trim().toUpperCase();
        ArrayList<DiemTT> dsDiemSearch = new ArrayList<>();
        for(var ds : dsDiemTT){
            if(ds.getMaSV().trim().toUpperCase().contains(maSVSearch)){
                dsDiemSearch.add(ds);
            }
        }
        jTableDiem.setModel(new DiemTTDAO(dsDiemSearch,dsSinhVien));
    }//GEN-LAST:event_btnTimKiemActionPerformed

