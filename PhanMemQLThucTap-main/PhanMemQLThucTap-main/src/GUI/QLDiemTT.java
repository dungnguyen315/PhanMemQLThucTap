    private void loadFileDiem() {
        try {
           dsDiemTT=(ArrayList<DiemTT>) dBEngine.docFile(fileDiem);
        } catch (Exception e) {
           JOptionPane.showMessageDialog(this,e);
        }
    }

    private void loadFileSV() {
        try {
           dsSinhVien=(ArrayList<SinhVien>) dBEngine.docFile(fileSV);
        } catch (Exception e) {
           JOptionPane.showMessageDialog(this,e);
        }
    }
    private void luuFileDiem(){
        try {
            dBEngine.LuuFile(fileDiem,dsDiemTT);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e);
        }
    }
    
    private void loadComBoxSV() {
        jComboBoxSV.removeAllItems();
        for (var sv : dsSinhVien) {
            jComboBoxSV.addItem(sv.getHoTen());
        }
    }

    private void loadTable() {
        jTableDiem.setModel(new DiemTTDAO(dsDiemTT,dsSinhVien));
    }

   
